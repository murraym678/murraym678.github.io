		function initialiseGel() {
			console.log('initialising gel');
			dnaBandsContainer.removeAllChildren();
			for (i=gSamples.length-1; i>LAST_LADDER_BAND; i--)
				gSamples.pop();
		
			switch (_this.primerEvaluation) {
				case PRIMERS_CREATE_NO_PRODUCT:
					break;
				case PRIMERS_CREATE_SMEAR:
					for (lane=1; lane<=4; lane++)
						gSamples.push( new DNA_Band(lane,1500,100) ); // i.e. smear over 500bp range
					break;
				case PRIMERS_CREATE_EXTRA_BANDS:
					for (lane=1; lane<=4; lane++) {
						switch (gReality[lane]) {
							case PLUS_PLUS:
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize+ALU_SIZE,0) );
								break
							case PLUS_MINUS:
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize,0) ); // hetero -ve/+ve
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize+ALU_SIZE,0) );
								break
							case MINUS_MINUS:
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize,0) ); // homo -ve
								break
						}
						// now add a few extra non-specific bands which will be in every lane
						gSamples.push( new DNA_Band(lane,520,0) ); // homo -ve
						gSamples.push( new DNA_Band(lane,710,0) ); // homo -ve
						gSamples.push( new DNA_Band(lane,1150,0) ); // homo -ve
					}
					break;
				case PRIMERS_EXCLUDE_ALU:
					for (lane=1; lane<=4; lane++)
						gSamples.push( new DNA_Band(lane,_this.pcrProductSize,0) );
					break;
				case PRIMERS_OK:
					for (lane=1; lane<=4; lane++) {
						switch (gReality[lane]) {
							case PLUS_PLUS:
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize+ALU_SIZE,0) );
								break
							case PLUS_MINUS:
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize,0) ); // hetero -ve/+ve
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize+ALU_SIZE,0) );
								break
							case MINUS_MINUS:
								gSamples.push( new DNA_Band(lane,_this.pcrProductSize,0) ); // homo -ve
								break
						}
					}
					break;
			}	
			
			for (i=0; i<gSamples.length; i++) 
			{
				var band = undefined;
				if (gSamples[i].smearSize > 0) {
					band = new lib.smear_band();
					band.scaleY = 0.014;
				} else
					band = new lib.spotty_band();
				band.x = gLanePosArr[ gSamples[i].lane ].x;
				band.y = gLanePosArr[ gSamples[i].lane ].y;
				dnaBandsContainer.addChild(band);
				if (gSamples[i].smearSize > 0)
					band.scaleX = 1.5
				else
					band.scaleX = 2.0;
			}
		}
		
