		function onMouseDown(evt){
			var item = evt.currentTarget;
			var ii = mapItemToCross(item);
			//console.log('mousedown on item '+item);
			if ((ii==-1) || okToMove(ii)) {
				item.offset = {x:0, y:0};
				var pt = item.parent.globalToLocal(evt.stageX, evt.stageY);
				item.offset.x = pt.x - item.x;
				item.offset.y = pt.y - item.y;
				item.drag = true;
			}
		}
		
		function onMouseMove(evt){
			var item = evt.currentTarget;
			var ii = mapItemToCross(item);
			//console.log('MoveEvent on  '+item);
			if ((ii==-1) || okToMove(ii)) {
				if (item.drag)
				{
					var pt = item.parent.globalToLocal(evt.stageX, evt.stageY);
					item.x = pt.x - item.offset.x;
					if (item != _this.myRuler) {
						item.y = pt.y - item.offset.y;
					}
				}
			}
		}
		
		function onMouseUp(evt){
			var item = evt.currentTarget;
			var ii = mapItemToCross(item);
			if ((ii==-1) || okToMove(ii)) {
				item.drag = false;
				if (ii > _this.lastCrossMoved)
					_this.lastCrossMoved = ii;
				removeLines();
				updateLines();
			}
		}
		
