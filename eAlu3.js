		function gelStepY(basepairs) {
			/*
			 y = mx + c (when plotted on log/linear graph
			 log(size) = m(dist_travelled) + c (note that the slope m is negative)
			 so dist_travelled = (log(size)-c)/m
			*/
			return (Math.log10(basepairs)-C_CONSTANT) / M_CONSTANT;
		}
		
		
