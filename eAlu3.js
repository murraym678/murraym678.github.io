		function showAmplicon(el,fwdFrom,fwdTo,revFrom,revTo) 
		// preserve one (or two substrings if revFrom>-1)) in the original innerHTML and set all the rest to spaces.
		{
			var s = el.textContent;
		    var newStr = '';
			var invisibleChar = String.fromCharCode(160);
			var fwdStr = s.substr(fwdFrom,fwdTo-fwdFrom+1);
			// do the spaces up to fwd and add the fwd string
		    for (i=0;i<fwdFrom;i++) {
		    	newStr += invisibleChar;
			}
		    newStr += fwdStr;
			
			if (revFrom == -1) {	// if revFrom == -1 then we do spaces to the end.
				for (i=fwdTo+1;i<s.length;i++)
					newStr += invisibleChar;		
			} else {	// otherwise do ...
				var revStr = s.substr(revFrom,revTo-revFrom+1);
				for (i=fwdTo+1;i<revFrom;i++)	// spaces for the middle bit...
					newStr += invisibleChar;	
				newStr += revStr;				// ...add the rev string and then...
				for (i=revTo+1;i<s.length;i++)	// ...spaces to end
					newStr += invisibleChar;		
			}
			var fredStr = newStr.slice();
		    el.textContent = fredStr;
		}
		
		
		function evaluatePrimers() {
			var allDNA = _this.textArea.textContent;
			var fPrimer = _this.fwdPrimerLabel.textContent;
			var rPrimer = reverseComplement(_this.revPrimerLabel.textContent);
			var fPos = _this.fwdPrimerPosition
			var rPos = _this.revPrimerPosition
			_this.pcrProductSize = (rPos+rPrimer.length)-fPos;
		//	_this.totalBPtext.text = _this.pcrProductSize;
			_this.totalBPlabel.textContent = _this.pcrProductSize;
			
			console.log('Evaluating primers lengths-> f='+fPrimer.length+' r='+rPrimer.length);
			if ((fPrimer.length<=PRIMER_WAY_TOO_SMALL) || (rPrimer.length<=PRIMER_WAY_TOO_SMALL)) {
				console.log('at least one is way too small <='+PRIMER_WAY_TOO_SMALL+' so result='+PRIMERS_CREATE_SMEAR);
				_this.primerEvaluation = PRIMERS_CREATE_SMEAR;
			} 
			else if ((fPrimer.length<=PRIMER_TOO_SMALL) || (rPrimer.length<=PRIMER_TOO_SMALL)) 
			{
				console.log('at least one is too small <='+PRIMER_TOO_SMALL+' so result='+PRIMERS_CREATE_EXTRA_BANDS);
				_this.primerEvaluation = PRIMERS_CREATE_EXTRA_BANDS;
			}
			else 
			{
				//can't have the end of either primer go past the start of the other - as this would mean no product
				if ((rPos < fPos) || (fPos+fPrimer.length > rPos+rPrimer.length)) {
					console.log('primers overlap so result='+PRIMERS_CREATE_NO_PRODUCT);
					_this.primerEvaluation = PRIMERS_CREATE_NO_PRODUCT;
				} else if ((fPos+fPrimer.length-1 <= ALU_INSERTION_POINT) && (rPos>ALU_INSERTION_POINT)) {
					console.log('primers OK so result='+PRIMERS_OK);
					_this.primerEvaluation = PRIMERS_OK;
				} else {
					console.log('primers DONT FLANK ALU INSERTION so result='+PRIMERS_EXCLUDE_ALU);
					_this.primerEvaluation = PRIMERS_EXCLUDE_ALU;
				}
			}
		}
		
		
